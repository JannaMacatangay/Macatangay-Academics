var total = 0;

function loadCategories() {
    var categoriesContainer = document.getElementById("categories");

    products.forEach((product, index) => {
        categoriesContainer.innerHTML += `
          <div onclick="loadProducts('`+ index + `')" class="card mx-1 custom-button p-3 text-center">
            <small><b>` + product.category + `</b></small>
          </div>
        `;
    });
}

function loadProducts(categoryIndex) {
    var maincontainer = document.getElementById("maincontainer");
    maincontainer.innerHTML = "";

    if (categoryIndex == 0 || categoryIndex == 1 || categoryIndex == 2) {
        products[categoryIndex].contents.forEach(content => {
            if (content.sizes && content.sizes.length > 0) {
                content.sizes.forEach(size => {
                    maincontainer.innerHTML += `
                      <div onclick="addToReceipt('` + size.price + `','` + content.code + size.code + `')" 
                           class="product-card card mx-1 my-2 custom-button content text-center">
                        <img src="images/clothes/` + content.imageFolder + `/` + content.image + `" 
                             alt="` + content.name + `" 
                             class="product-image img-fluid mb-2">
                        <small class="product-name">` + content.name + ' ' + size.code + `</small>
                      </div>
                    `;
                });
            } else {
                maincontainer.innerHTML += `
                  <div onclick="addToReceipt('` + content.price + `','` + content.code + `')" 
                       class="product-card card mx-1 my-2 custom-button content text-center">
                    <img src="images/clothes/` + content.imageFolder + `/` + content.image + `" 
                         alt="` + content.name + `" 
                         class="product-image img-fluid mb-2">
                    <small class="product-name">` + content.name + `</small>
                  </div>
                `;
            }
        });
    }

    else if (categoryIndex == 3) {
        products[categoryIndex].contents.forEach(content => {
            maincontainer.innerHTML += `
              <div onclick="addToReceipt('` + content.price + `','` + content.code + `')" 
                   class="product-card card mx-1 my-2 custom-button content text-center">
                <img src="images/clothes/` + content.imageFolder + `/` + content.image + `" 
                     alt="` + content.name + `" 
                     class="product-image img-fluid mb-2">
                <small class="product-name">` + content.name + `</small>
              </div>
            `;
        });
    }
}

function addToReceipt(price, code) {
    var receiptContainer = document.getElementById("receipt");
    total = parseFloat(total) + parseFloat(price);

    var totalValueElement = document.getElementById("totalValue");
    if (totalValueElement) {
        totalValueElement.innerHTML = total.toFixed(2); 
    }

    receiptContainer.innerHTML += `
        <div class="d-flex flex-row justify-content-between">
          <div><small>`+ code + `</small></div>
          <div><small>`+ price + `</small></div>
        </div>
      `;
}

window.onload = function () {
    const totalValueElement = document.getElementById('totalValue');
    if (totalValueElement) {
        totalValueElement.textContent = total.toFixed(2); 
    }
};

